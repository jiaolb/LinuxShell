import math as m

#向上取整
print (m.ceil(-1.5))

#向下取整
print (m.floor(-1.5))

#返回数字的绝对值
print (m.fabs(-1.5))

print (m.log(33,2))

print (m.sqrt(2))


print (m.exp(1))

#返回x的整数部分与小数部分，两部分的数值符号与x相同，整数部分以浮点型表示。
print (m.modf(3.222))

#x**y 运算后的值。
print (m.pow(2,5))






